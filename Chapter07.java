
public class Chapter07 {
	public static void main(String[] args) {
		// 問題1
		int dt = 90;
		if (dt <= 5) {
			System.out.println("とても近いです");
		} else if (dt <= 10) {
			System.out.println("まぁ近いです");
		} else if (dt <= 15) {
			System.out.println("まぁ遠いです");
		} else {
			System.out.println("とても遠いです");
		}

	}
}
